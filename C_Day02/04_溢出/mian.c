#include<stdio.h>
int main(void) {
	// short 两个字节
	// 一个字节byte=8bit
	short i = 32767, j;
	// i = 0111 1111 1111 1111 7f(h)ff(l)
	// 但由于是小端模式，按字节从低位对齐低地址
	// 小端存储：数据高字节保存在内存的高地址中，数据的低字节保存在低地址中。
	// 0x00000087A74FF5B4  ff 7f 00 00
	// +1 = 1000 0000 0000 0000 = -32768
	// short没有32768 0 - 32767 -1 - -32768
	// 溢出 i + 2 = -32767
	j = i + 1;
	printf("j = %d\n", j);
	return 0;
}